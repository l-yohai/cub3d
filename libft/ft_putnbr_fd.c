#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (!fd)
		return ;
	if (n >= 0)
	{
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
	else
	{
		ft_putchar_fd('-', fd);
		if (n < -9)
			ft_putnbr_fd(n / (-10), fd);
		ft_putchar_fd((n % 10 * -1) + '0', fd);
	}
}
